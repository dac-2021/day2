function sum(num1, num2) {
  let calculation = num1 + num2;

  return calculation;
}

let n1 = sum(10, 20);
console.log(`SUM is ${n1}`);

function difference(num1, num2) {
  let calculation = num1 - num2;

  return calculation;
}

let n2 = difference(10, 20);
console.log(`DIFFERENCE is ${n2}`);
